     * Display a listing of the resource.
     */
    public function index()
    {
        $search = request()->all()['search'] ?? '';
        $expenses = Expense::with(['salary', 'category'])
            ->where('value', 'LIKE', "$search%")
            ->paginate(6)
            ->onEachSide(0)
            ->withQueryString();

        return Inertia::render('expense/Index', [
            'expenses' => $expenses,
            'search' => $search
        ]);
    }

